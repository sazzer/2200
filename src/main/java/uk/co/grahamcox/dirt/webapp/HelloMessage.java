package uk.co.grahamcox.dirt.webapp;

/**
 * Message to say Hello with
 */
public class HelloMessage {
    /** The name to say hello to */
    private String name;

    /**
     * Get the name
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Set the name
     * @param name the name
     */
    public void setName(final String name) {
        this.name = name;
    }
}
